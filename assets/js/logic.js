class Despesa { // Criação de uma classe para referenciar o objeto "despesas"

    constructor(ano, mes, dia, tipo, descricao, valor) { // constructor recebendo parâmetros que serão recuperados na criação de um novo objeto...

        // atributos do objeto "despesas"
        this.ano = ano
        this.mes = mes
        this.dia = dia
        this.tipo = tipo
        this.descricao = descricao
        this.valor = valor
    }
}